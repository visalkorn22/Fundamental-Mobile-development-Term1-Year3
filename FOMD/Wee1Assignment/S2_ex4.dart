void main() {
  // String Example
  const String greeting = "Hello, Teacher Ronan";
  print(greeting); 

  // List Example
  const List<String> fruits = ["Apple", "Banana", "Cherry"];
  print(fruits); 

  print("First fruit: ${fruits[0]}"); 

  // Map Example
  const Map<String, int> fruitQuantities = {
    "Apple": 10,
    "Banana": 5,
  };
  print(fruitQuantities); 

  print("Banana quantity: ${fruitQuantities["Banana"]}");

}
